    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

    String fullname = fullnameField.getText();
    String email = emailField.getText();
    String password = new String(passwordField.getPassword());

    String checkEmailSQL = "SELECT email FROM Users WHERE email = ?";
    try (Connection con = DatabaseConnector.getConnection();
         PreparedStatement checkEmailStmt = con.prepareStatement(checkEmailSQL)) {

        // Check exist email
        checkEmailStmt.setString(1, email);
        ResultSet rs = checkEmailStmt.executeQuery();
        if (rs.next()) {
            init();
            JOptionPane.showMessageDialog(this, "Email already registered!");
        } else {
            String otp = generateOTP();
            //Send email
            Email.sendEmail(otp, email);
                       
            
            VerifyCode vFrame = new VerifyCode(fullname, email, password, otp);
            vFrame.setVisible(true);
            vFrame.pack();
            vFrame.setLocationRelativeTo(null); 
            init();
            this.dispose();
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error during registration!");
    }
    
    }//GEN-LAST:event_jButton1ActionPerformed
        
     public String generateOTP() {
            Random random = new Random();
            int otp = random.nextInt(999999); 
            return String.format("%06d", otp); 
        }
        
